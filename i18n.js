  function normalizeLang(lang) {
    return LANGUAGE_MAP[lang] ? lang : DEFAULT_LANG;
  }

  function getLangConfig(lang) {
    return LANGUAGE_MAP[normalizeLang(lang)] || LANGUAGE_MAP[DEFAULT_LANG];
  }

  function getLocale(lang) {
    return LOCALES[normalizeLang(lang)] || LOCALES[DEFAULT_LANG];
  }

  function getUiStrings(lang) {
    return { ...LOCALES[DEFAULT_LANG].ui, ...getLocale(lang).ui };
  }

  function getMenuStrings(lang) {
    return { ...LOCALES[DEFAULT_LANG].menu, ...getLocale(lang).menu };
  }

